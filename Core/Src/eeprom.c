/* Includes ------------------------------------------------------------------*/
#include "eeprom.h"
/* Defines -------------------------------------------------------------------*/
/* Variables -----------------------------------------------------------------*/
extern I2C_HandleTypeDef hi2c1;
/* Private Function definition -----------------------------------------------*/
/* Public Function -----------------------------------------------------------*/
void EEPROM_Write(uint16_t MemoryAddress, void* Value)
{
	HAL_I2C_Mem_Write(&hi2c1, (84<<1), MemoryAddress, I2C_MEMADD_SIZE_16BIT, Value, 2, 1000);
}

void EEPROM_Read(uint16_t MemoryAddress, void* Value)
{
	HAL_I2C_Mem_Read(&hi2c1, (84<<1), MemoryAddress, I2C_MEMADD_SIZE_16BIT, Value, 2, 1000);
}
